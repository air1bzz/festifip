var lastElem = null;

function bandMain(bandTag) {
  var extra = document.getElementsByClassName("extra")
  if (extra.length > 0) {
    extra[0].remove()
  }
  var band = bands[bandTag];
  var imgElem = document.getElementById("band-img");
  var titleElem = document.getElementById("band-title");
  var descElem = document.getElementById("band-description");
  var dateElem = document.getElementById("band-date");
  var mainElem = document.getElementById("band");
  var navElem = document.getElementById(bandTag);
  var src = "/assets/img/" + bandTag + ".jpg";

  titleElem.innerText = band.title;
  descElem.innerText = band.desc;
  dateElem.innerText = band.date;
  imgElem.setAttribute("src", src);
  imgElem.setAttribute("alt", bandTag);

  // clean text children
  for (
    var elem = document.getElementsByClassName("band-text")[0];
    document.getElementsByClassName("band-text").length !== 0;
    elem = document.getElementsByClassName("band-text")[0]
  ) {
    elem.remove();
  }

  for (var text of band.para) {
    var div = document.createElement("div");
    div.setAttribute("class", "band-text");
    div.innerText = text;
    mainElem.appendChild(div);
  }

  if (band.links) {
    for (var link of band.links) {
      var a = document.createElement("a");
      a.setAttribute("class", "band-text");
      a.setAttribute("href", link);
      a.setAttribute("target", "_blank");
      a.innerText = link;
      mainElem.appendChild(a);
    }
  }

  // Change color
  if (lastElem) {
    lastElem.firstElementChild.style.color = "#9E7B9F";
  }
  navElem.firstElementChild.style.color = "#E6B552";
  lastElem = navElem;
}
